# Правила валидации форматов
def validate_entity(text: str, label: str) -> Tuple[bool, str]:
    """
    Проверяет соответствие текста сущности её типу.
    Returns: (is_valid, reason)
    """
    text = text.strip()

    if label == "Email":
        if "@" not in text:
            return False, "Email без @"
        if not re.search(r'@[\w.-]+\.\w+', text):
            return False, "Некорректный формат email"

    elif label == "Номер телефона":
        digits = re.sub(r'\D', '', text)
        if len(digits) < 7:
            return False, f"Слишком мало цифр: {len(digits)}"

    elif label == "CVV/CVC":
        digits = re.sub(r'\D', '', text)
        if not (3 <= len(digits) <= 4):
            return False, f"CVV должен быть 3-4 цифры, got {len(digits)}"

    elif label == "ПИН код":
        digits = re.sub(r'\D', '', text)
        if len(digits) != 4:
            return False, f"ПИН должен быть 4 цифры, got {len(digits)}"

    elif label == "ФИО":
        if not re.search(r'[а-яА-ЯёЁa-zA-Z]', text):
            return False, "ФИО без букв"
        if re.match(r'^[\d\s\-]+$', text):
            return False, "ФИО только цифры"

    elif label == "СНИЛС клиента":
        digits = re.sub(r'\D', '', text)
        if len(digits) != 11:
            return False, f"СНИЛС должен быть 11 цифр, got {len(digits)}"

    elif label == "Сведения об ИНН":
        digits = re.sub(r'\D', '', text)
        if len(digits) not in [10, 12]:
            return False, f"ИНН должен быть 10 или 12 цифр, got {len(digits)}"

    elif label == "Номер карты":
        digits = re.sub(r'\D', '', text)
        if not (13 <= len(digits) <= 19):
            return False, f"Карта должна быть 13-19 цифр, got {len(digits)}"

    elif label == "Номер банковского счета":
        digits = re.sub(r'\D', '', text)
        if len(digits) != 20:
            return False, f"Счёт должен быть 20 цифр, got {len(digits)}"

    elif label == "Одноразовые коды":
        digits = re.sub(r'\D', '', text)
        if not (4 <= len(digits) <= 8):
            return False, f"OTP обычно 4-8 цифр, got {len(digits)}"

    elif label == "Дата рождения" or label == "Дата окончания срока действия карты":
        if not re.search(r'\d', text):
            return False, "Дата без цифр"

    return True, "OK"

