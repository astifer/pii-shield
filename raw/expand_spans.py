"""## Hypothesis 2: Overlapping Spans

Пересекающиеся спаны - оставляем больший, удаляем меньший
"""

def find_and_remove_overlapping(preds_df, texts_df):
    """
    Находит пересекающиеся спаны и оставляет только больший.
    Возвращает новый DataFrame и список удалённых спанов.
    """
    results = []
    removed_list = []

    for idx, row in preds_df.iterrows():
        pred_id = row['id']
        pred_str = row['Prediction']

        try:
            entities = ast.literal_eval(pred_str)
        except:
            entities = []

        if not entities:
            results.append({'id': pred_id, 'Prediction': '[]'})
            continue

        text = texts_df.loc[texts_df['id'] == pred_id, 'text'].values[0]

        # Сортируем по началу, потом по длине (больший первый)
        sorted_ents = sorted(entities, key=lambda x: (x[0], -(x[1] - x[0])))

        filtered = []
        for ent in sorted_ents:
            start, end, label = ent[0], ent[1], ent[2]
            span_text = text[start:end]

            # Проверяем пересечение с уже добавленными
            overlaps_with = None
            for existing in filtered:
                ex_start, ex_end, ex_label = existing[0], existing[1], existing[2]
                # Пересекаются если один начинается до конца другого
                if start < ex_end and end > ex_start:
                    overlaps_with = existing
                    break

            if overlaps_with:
                ex_start, ex_end, ex_label = overlaps_with
                ex_len = ex_end - ex_start
                cur_len = end - start

                if cur_len > ex_len:
                    # Текущий больше - удаляем старый, добавляем текущий
                    filtered.remove(overlaps_with)
                    filtered.append(ent)
                    removed_list.append({
                        'id': pred_id,
                        'removed': (ex_start, ex_end, ex_label),
                        'removed_text': text[ex_start:ex_end],
                        'kept': (start, end, label),
                        'kept_text': span_text,
                    })
                else:
                    # Старый больше или равен - удаляем текущий
                    removed_list.append({
                        'id': pred_id,
                        'removed': (start, end, label),
                        'removed_text': span_text,
                        'kept': overlaps_with,
                        'kept_text': text[ex_start:ex_end],
                    })
            else:
                filtered.append(ent)

        results.append({'id': pred_id, 'Prediction': str(filtered)})

    return pd.DataFrame(results), removed_list

