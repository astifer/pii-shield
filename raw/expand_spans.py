LABELS_TO_FILTER_SHORT_LETTERS = {
    'Дата регистрации по месту жительства или пребывания',
    'Дата окончания срока действия карты',
}

def is_only_letters(text):
    """Проверяет, состоит ли текст только из букв."""
    return text.strip().isalpha()

def should_remove_short_span(text, label, max_len=3):
    """
    Проверяет, нужно ли удалить короткий спан.
    Удаляем если:
    - длина <= max_len
    - класс в списке фильтрации
    - текст состоит только из букв (например "с")
    """
    if len(text) > max_len:
        return False
    if label not in LABELS_TO_FILTER_SHORT_LETTERS:
        return False
    if is_only_letters(text):
        return True
    return False

def remove_short_letter_spans(preds_df, texts_df):
    """Удаляет короткие буквенные спаны и возвращает новый DataFrame."""
    results = []
    removed_list = []

    for idx, row in preds_df.iterrows():
        pred_id = row['id']
        pred_str = row['Prediction']

        try:
            entities = ast.literal_eval(pred_str)
        except:
            entities = []

        if not entities:
            results.append({'id': pred_id, 'Prediction': '[]'})
            continue

        text = texts_df.loc[texts_df['id'] == pred_id, 'text'].values[0]

        new_entities = []
        for entity in entities:
            start, end, label = entity[0], entity[1], entity[2]
            span_text = text[start:end]

            if should_remove_short_span(span_text, label):
                ctx_start = max(0, start - 15)
