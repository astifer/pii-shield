# Символы-разделители (на них останавливаемся при расширении)
STOP_CHARS = set(' \t\n.,;:!?()[]{}"\'-—–«»')

def expand_span_to_word_boundary(text: str, start: int, end: int) -> Tuple[int, int]:
    """
    Расширяет спан до границ слов (не включая пунктуацию и кавычки).
    """
    # Расширяем влево до разделителя
    new_start = start
    while new_start > 0 and text[new_start - 1] not in STOP_CHARS:
        new_start -= 1

    # Расширяем вправо до разделителя
    new_end = end
    while new_end < len(text) and text[new_end] not in STOP_CHARS:
        new_end += 1

    return new_start, new_end

