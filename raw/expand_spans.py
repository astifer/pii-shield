def expand_all_predictions(preds_df: pd.DataFrame, texts_df: pd.DataFrame) -> Tuple[pd.DataFrame, List[dict], List[dict]]:
    """
    Расширяет все предсказанные спаны до границ слов.
    Удаляет спаны, состоящие только из запятой.
    Возвращает DataFrame с расширенными предсказаниями, список изменений и список удалённых.
    """
    expanded_predictions = []
    changes = []
    removed = []

    for idx, row in preds_df.iterrows():
        pred_id = row['id']
        pred_str = row['Prediction']

        # Парсим предсказания
        try:
            entities = ast.literal_eval(pred_str)
        except:
            entities = []

        if not entities:
            expanded_predictions.append({'id': pred_id, 'Prediction': '[]'})
            continue

        # Получаем текст
        text = texts_df.loc[texts_df['id'] == pred_id, 'text'].values[0]

        new_entities = []
        for entity in entities:
            start, end, label = entity[0], entity[1], entity[2]
            span_text = text[start:end]

            # Удаляем спаны состоящие только из запятой или пунктуации
            if span_text.strip() in [',', '.', ';', ':', '!', '?', '']:
                removed.append({
                    'id': pred_id,
                    'label': label,
                    'span': (start, end),
                    'text': span_text,
                })
                continue  # Пропускаем этот спан

            new_start, new_end = expand_span_to_word_boundary(text, start, end)

            # Записываем изменение если спан изменился
            if new_start != start or new_end != end:
                changes.append({
                    'id': pred_id,
                    'label': label,
                    'old_span': (start, end),
                    'new_span': (new_start, new_end),
                    'old_text': span_text,
                    'new_text': text[new_start:new_end],
                })

            new_entities.append((new_start, new_end, label))

        expanded_predictions.append({'id': pred_id, 'Prediction': str(new_entities)})

    result_df = pd.DataFrame(expanded_predictions)
    return result_df, changes, removed

