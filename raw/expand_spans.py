"""## Hypothesis 5: Suspicious Patterns

Детекция неестественных паттернов:
- Сущности, содержащие только стоп-слова
- Сущности, начинающиеся/заканчивающиеся пунктуацией
- Сущности с подозрительным контекстом
"""

# Детекция подозрительных паттернов
STOP_WORDS = {'и', 'в', 'на', 'с', 'по', 'к', 'от', 'до', 'за', 'из', 'у', 'о', 'для', 'при', 'а', 'но', 'или', 'что', 'как', 'это', 'то', 'же', 'все', 'так'}
PUNCT_CHARS_SET = set('.,;:!?()[]{}"\'-')

def detect_suspicious(entity):
    """Детектирует подозрительные паттерны."""
    text = entity['text']
    label = entity['label']
    issues = []

    # Начинается/заканчивается пунктуацией
    if text and text[0] in PUNCT_CHARS_SET:
        issues.append(f"Starts with punct: '{text[0]}'")
    if text and text[-1] in PUNCT_CHARS_SET and label not in ['Email']:
        issues.append(f"Ends with punct: '{text[-1]}'")

    # Содержит только стоп-слова
    words = set(text.lower().split())
    if words and words.issubset(STOP_WORDS):
        issues.append("Only stop-words")

    # Содержит "..." или много пробелов
    if '...' in text:
        issues.append("Contains '...'")
    if '  ' in text:
        issues.append("Contains double spaces")

    # Для числовых сущностей - проверяем наличие букв где не должно быть
    numeric_labels = ['CVV/CVC', 'ПИН код', 'СНИЛС клиента', 'Сведения об ИНН', 'Номер банковского счета']
    if label in numeric_labels:
        cleaned = re.sub(r'[\s\-]', '', text)
        if re.search(r'[а-яА-ЯёЁa-zA-Z]', cleaned):
            issues.append("Letters in numeric field")

    # Подозрительно короткое для текстовых сущностей
